import math
angulo = float(input('Digite um ângulo: '))
radiano = math.radians(angulo)
print(f'O seno é: {math.sin(radiano):.2f} \n'
      f'O cosseno é: {math.cos(radiano):.2f} \n'
      f'A tangente é: {math.tan(radiano):.2f}.')



#O programa deve ler um ângulo e mostrar o seno, cosseno e tangente.